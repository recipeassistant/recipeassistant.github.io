/////////// Merges ingredients ///////////////
function compare(arr) {
  const hash = new Map();

  // Loop through every ingredient
  for (let i = 0; i < arr.length; i++) {
    for (let j = 0; j < arr[i].length; j++) {
      // if the ingredient hasn't been hashed already, then hash the object
      // e.g. garlic --> {name: "Garlic", quantity: 3, unit: "pieces"}
      if (hash.get(arr[i][j].name) === undefined) {
        hash.set(arr[i][j].name, arr[i][j]);
      }
      // if it's already been hashed, then add the quantities together
      else {
        let quantity = hash.get(arr[i][j].name).quantity;

        hash.set(arr[i][j].name, {
          name: arr[i][j].name,
          quantity: quantity + arr[i][j].quantity,
          unit: arr[i][j].unit,
        });
      }
    }
  }
  return hash;
}

