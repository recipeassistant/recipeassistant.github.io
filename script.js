function hashPrices() {
  const hash = new Map();

  // Loop and hash every ingredient prices object to a reference of the ingredient name
  // e.g. Flour - > {name: "Flour", price: 2.67, perUnit: 500}
  for (let i = 0; i < ingredientPrices.length; i++) {
    hash.set(ingredientPrices[i].name, ingredientPrices[i]);
  }

  return hash;
}

